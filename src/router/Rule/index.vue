            <h4>2.预期动作（Anticipation）</h4>
            <p>
                物体从静止到开始动作前会有所谓的预期动作，可让观众能预知下个动作。
            </p>
            <p>
                例如从高空往下跳跃时会先弯曲膝盖再跳、正在跑步的人要停止跑步前会逐渐变慢步伐等等。在动画中适当加入预期动作不仅可增添生动性，亦可给予观众惊喜性。
            </p>
