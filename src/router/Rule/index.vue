          </iframe>
        </section>
        <section>
            <h4>6.渐快与渐慢（Slow in and slow out）</h4>
            <p>
                物体从静止到移动是渐进加速，移动到静止则是渐进减速，也就是说物体从开始到结束移动并不会是等速度运动。
            </p>
            <p>
                例如一辆车子不管马力再怎么快，开始移动是渐进加速的，停止移动也会渐缓减速。
            </p>

        </section>
        <section>
            <h4>7.弧形（Arcs）</h4>
            <p>
                凡是具有生命的物体，其移动、动作都不会是完美的直线行进，因此作画时要以自然的弧形方式呈现运动。
            </p>
            <p>
                例如走路绝不会是笔直的前进、头部转动也都是弧形方式。
            </p>
