            </p>
            <p>
                例如一个具有弹性的球，从空中垂直瞬间落地时，球体会被下向挤压成水平椭圆体，下一个瞬间反弹至空中时，球体会被向上伸展成垂直椭圆体，动画亦以这样的方式呈现真实感，若附加夸张非写实的变形亦增加动画的生动与趣味性。
            </p>
