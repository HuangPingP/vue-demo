          </iframe>
        </section>
        <section>
            <h4>8.附属动作（Secondary action）</h4>
            <p>
                主体在动作时，身上若有其他附加配件也会以自然的方式动作，忽略了这些配件动作会使得整体不自然。
            </p>
            <p>
                例如穿戴的耳环会晃动、尾巴会摇摆、外套会飘逸等等。
            </p>
        </section>
        <section>
            <h4>9.时间控制（Timing）</h4>
            <p>
                控制好物体的动作时间即是动画的灵魂，亦是表现动画节奏的关键。
            </p>
            <p>
                例如往前出一直拳的动作是瞬间的、在湖中划船是缓慢前进的。
            </p>
        </section>
        <section>
            <h4>10.夸张（Exaggeration）</h4>
            <p>
                配合前面的挤压与伸展，加上夸张不符合现实的现象亦是增添动画的张力。
            </p>
            <p>
                例如在动画中一个人的脸被揍都会往内凹深得很严重、往前冲刺会附加许多的华丽效果、迅速躲过子弹等等。
            </p>
        </section>
        <section>
            <h4>11.纯熟的手绘技巧（Solid drawing）</h4>
            <p>
                传统动画都是纯手工绘制画面，亦即手绘技巧越好，动画呈现就越精致完美。直到近代电脑绘制动画也是需要一定的手绘能力。
            </p>
            <p>
                例如人物结构、骨架、造型、动作、透视、背景等等。
            </p>
        </section>
        <section>
            <h4>12.吸引力（Appeal）</h4>
            <p>
                一部好的动画作品一定会有令人吸引的人事物，例如人物外型鲜明、表态具有特色、完整的高成迭起剧情都可吸引观众。
            </p>
        </section>
